  <h4>Important note for students receiving needs-based financial aid:</h4> 
  <p>  
    Financial assistance from all sources during the academic year
    (September thru August), including prize money earned from Hopwood Award contests, factors into the ongoing
    review of students' financial aid eligibility now and in the future. If your total financial aid, including any new offer,
    exceeds your eligibility for financial assistance this academic year, your aid may be limited in a future term or reduced from previously
    received assistance. You can learn more about financial aid terms and conditions on the Office of Financial Aid's <a href="https://finaid.umich.edu/" target="_blank">website</a>.
    If you have questions or want to consult with someone about your particular situation, please contact OFA.
